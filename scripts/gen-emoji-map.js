#!/usr/bin/env node
// Fetch the rxaviers gist raw markdown and parse emoji glyphs and shortnames
// Then write a static src/utils/emojiMap.js file with a complete EMOJI_MAP.

import fs from 'fs'
import path from 'path'
// Use node-emoji's dataset to build a static map. This covers the majority of
// GitHub shortnames and is reliable offline. Import dynamically to interop with
// CommonJS package shape.
async function buildMapFromNodeEmoji() {
  const pkg = await import('node-emoji')
  const data = pkg.emoji || (pkg.default && pkg.default.emoji) || pkg.default || pkg
  const map = Object.create(null)
  // pkg.emoji is an object mapping names to glyphs; if not present, try pkg.default
  const source = data && data.emoji ? data.emoji : data
  for (const name of Object.keys(source || {})) {
    map[name] = source[name]
  }
  return map
}

function renderFile(map) {
  const entries = Object.keys(map).sort().map(k => {
    const v = map[k]
    const safe = v.replace(/\\/g, '\\\\').replace(/'/g, "\\'")
    return `  '${k}': '${safe}',`
  }).join('\n')

  return `// This file is autogenerated by scripts/gen-emoji-map.js
// Source: node-emoji (derived from common shortnames)

export const EMOJI_MAP = {
${entries}
}

const SHORTCODE_RE = /:([a-z0-9_+\\-]+):/gi

export function replaceShortcodes(text) {
  return String(text ?? '').replace(SHORTCODE_RE, (m, name) => EMOJI_MAP[name] || m)
}

export default { EMOJI_MAP, replaceShortcodes }
`
}

async function main() {
  try {
    const map = await buildMapFromNodeEmoji()
    // Ensure a few canonical aliases are present
    if (!map['+1'] && map['thumbsup']) map['+1'] = map['thumbsup']
    const out = renderFile(map)
    const outPath = path.resolve(process.cwd(), 'src/utils/emojiMap.js')
    fs.writeFileSync(outPath, out, 'utf8')
    console.log('Wrote', outPath, 'entries=', Object.keys(map).length)
  } catch (e) {
    console.error('Failed to generate emoji map:', e)
    process.exit(1)
  }
}

main()
